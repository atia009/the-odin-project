
// functions
function startAdd(num1, num2) {
    return num1 + num2;
}

function startSubtract(num1, num2) {
    return num1 - num2;
}

function startMultiply(num1, num2) {
    return num1 * num2;
}

function startDivide(num1, num2) {
    return num1 / num2;
}

// test
console.log(startAdd(4, 2));
console.log(startSubtract(4, 2));
console.log(startMultiply(4, 2));
console.log(startDivide(4, 2));